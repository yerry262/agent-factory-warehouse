# Pad to minimum length so offset math works (Windows DigitalProductId is normally 164 bytes)
# This keeps missing bytes as 0 so the script still runs for your partial sample
while len(KEY_BYTES) < 67:
    KEY_BYTES.append(0)

# Windows key decoding alphabet
KEYMAP = "BCDFGHJKMPQRTVWXY2346789"

def decode_windows_key(dpid):
    key_start = 52
    key_end = key_start + 15
    decode_bytes = dpid[key_start:key_end]

    decoded_chars = [''] * 25

    for i in range(24, -1, -1):
        current = 0
        for j in range(14, -1, -1):
            current = current * 256
            current += decode_bytes[j]
            decode_bytes[j] = current // 24
            current = current % 24
        decoded_chars[i] = KEYMAP[current]

    # Insert hyphens
    key = ''.join(decoded_chars)
    return "-".join([key[i:i+5] for i in range(0, 25, 5)])

# Decode the key
decoded = decode_windows_key(KEY_BYTES)
print("Decoded Windows Product Key:", decoded)